    protected function send($params)
    {
        $res = $this->asPost($this->host, ['JSONData'=> json_encode($params)], []);
        if($res['code'] === 200)
        {   // 출금 요청 성공시
            $res = $res['body'];
            if($res['RESP_CD'] === "0000")
            {   // 응답 코드가 0000일때
                if($params['KEY'] === "6120")
                    $res['RESP_MSG'] = "이체 요청";
                else if($params['KEY'] === "6140")
                    $res['RESP_MSG'] = "잔액 조회 성공";
                else if($params['KEY'] === "6170")
                {   // 응답키가 6170이면서 상태가 1이면 이체 성공
                    if($res['STS'] === 1)
                        $res['RESP_MSG'] = "이체 성공";
                    else if($res['STS'] === 3)
                    {   // 상태가 3이면 에러
                        $res['RESP_CD'] === "-1000";
                        $res['RESP_MSG'] = "이체 에러";
                    }
                    else if($res['STS'] === 6)
                        $res['RESP_MSG'] = "이체 처리중";
                }
            }
            else
            {
                $res['RESP_MSG'] = isset($res['RESP_MSG']) ? $res['RESP_MSG'] : '';
                $res['RESP_MSG'] = $this->getErrorMessage($res['RESP_CD'], $res['RESP_MSG']);
            }
            return $res;
        }
        else
        {   // 출금 요청 실패시
            $res = $res['body'];
            $message = isset($res['RESP_MSG']) ? $res['RESP_MSG'] : '타임아웃';
            $body = [
                'RESP_CD' => '-500',
                'RESP_MSG' => $message,
            ];

            Log::warning("coocon finance van warning", array_merge([
                'key' => $params['KEY']
            ], $res['body']));

            return $body;
        }
    }

