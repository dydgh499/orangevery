    protected function getAccountAuthAESEncrypt($value)
    {
        $enc_data = base64_encode(
            openssl_encrypt(
                $value,
                'AES-256-CBC',
                $this->finance_van['enc_key'],
                OPENSSL_RAW_DATA,
                str_repeat(chr(0), 16)
            )
        );
        return urlencode(utf8_encode($enc_data));
    }

