    protected function getAccountAuthHmacSha256($value)
    {
        $enc_data = base64_encode(
            hash_hmac(
                'sha256',
                $value,
                $this->finance_van['enc_key'],
                true
            )
        );
        return urlencode(utf8_encode($enc_data));
    }

    protected function verifyAccountAuthHmac($value, $hmac)
    {
        $data = $this->getAccountAuthAESDecrypt($value);
        //데이터 복호화 AES256
        $check_hmac = $this->getAccountAuthHmacSha256(substr($data, 14));
        if($hmac === $check_hmac)
            return false;
        else
            return true;
    }

